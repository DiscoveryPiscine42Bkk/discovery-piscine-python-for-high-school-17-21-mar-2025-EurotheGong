minus_number = first_number - second_number
multiply_number = first_number * second_number
divide_number = first_number // second_number
print(f"{first_number} + {second_number} = {plus_number}")
print(f"{first_number} - {second_number} = {minus_number}")
print(f"{first_number} * {second_number} = {multiply_number}")
print(f"{first_number} / {second_number} = {divide_number}")